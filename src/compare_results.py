"""
compare_results.py - Build comparison tables/plots required by the project:

  1. Proposed model (Transformer-LSTM) vs. baseline (plain LSTM) — for EACH
     dataset. This directly satisfies the project objective: "To compare the
     proposed model with simple baseline models."
  2. Proposed model performance across BOTH datasets (cicdarknet2020 and
     unsw_nb15) — satisfies using >=2 datasets for comparison.

Usage:
    python src/compare_results.py

Reads results/<dataset>/<model_type>/metrics.json (generated by evaluate.py)
and writes:
  results/comparison/baseline_vs_proposed.csv
  results/comparison/baseline_vs_proposed.txt
  results/comparison/dataset_comparison.csv
  results/comparison/dataset_comparison.txt
  figures/comparison/baseline_vs_proposed_<dataset>.png
  figures/comparison/dataset_comparison_proposed_model.png
"""

import sys
from pathlib import Path

import pandas as pd
import matplotlib.pyplot as plt
import matplotlib
matplotlib.use("Agg")

sys.path.insert(0, str(Path(__file__).resolve().parent))

from utils import setup_logging, ensure_dir, load_metrics_json, save_text_report, save_plot

logger = setup_logging("compare_results")

DATASET_NAMES = ["cicdarknet2020", "unsw_nb15"]
MODEL_TYPES   = ["transformer_lstm", "baseline_lstm"]
METRICS_KEYS  = ["accuracy", "precision", "recall", "f1_score"]


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _try_load(dataset_name: str, model_type: str):
    path = Path("results") / dataset_name / model_type / "metrics.json"
    if not path.exists():
        logger.warning(
            "Metrics not found: %s  (run: python src/train.py --dataset_path dataset/<file> "
            "--dataset_name %s --model_type %s   then evaluate.py with the same args)",
            path, dataset_name, model_type,
        )
        return None
    return load_metrics_json(str(path))


def _row(m: dict) -> dict:
    row = {
        "Dataset":   m.get("dataset_name", "unknown"),
        "Model":     m.get("model_type", "unknown"),
        "Accuracy":  round(m.get("accuracy",  0.0), 4),
        "Precision": round(m.get("precision", 0.0), 4),
        "Recall":    round(m.get("recall",    0.0), 4),
        "F1-Score":  round(m.get("f1_score",  0.0), 4),
    }
    if "roc_auc" in m:
        row["ROC-AUC"] = round(m["roc_auc"], 4)
    return row


def _bar_chart(df: pd.DataFrame, group_col: str, title: str, save_path: str) -> None:
    metric_cols = [c for c in ["Accuracy", "Precision", "Recall", "F1-Score", "ROC-AUC"] if c in df.columns]
    x = range(len(metric_cols))
    groups = df[group_col].tolist()
    n_groups = len(groups)

    fig, ax = plt.subplots(figsize=(10, 6))
    bar_width = 0.35
    offsets = [bar_width * (i - n_groups / 2 + 0.5) for i in range(n_groups)]
    colors = plt.cm.tab10.colors

    for idx, (_, row) in enumerate(df.iterrows()):
        values = [row.get(m, 0.0) for m in metric_cols]
        bars = ax.bar(
            [xi + offsets[idx] for xi in x], values, width=bar_width,
            label=row[group_col], color=colors[idx % len(colors)],
            alpha=0.85, edgecolor="white",
        )
        for bar, val in zip(bars, values):
            ax.text(bar.get_x() + bar.get_width() / 2, bar.get_height() + 0.005,
                     f"{val:.3f}", ha="center", va="bottom", fontsize=8)

    ax.set_xticks(list(x))
    ax.set_xticklabels(metric_cols, fontsize=11)
    ax.set_ylim(0, 1.12)
    ax.set_ylabel("Score", fontsize=12)
    ax.set_title(title, fontsize=14, fontweight="bold")
    ax.legend(title=group_col, fontsize=10)
    ax.grid(axis="y", alpha=0.3)
    fig.tight_layout()
    save_plot(fig, save_path)
    logger.info("Saved chart → %s", save_path)


def _format_table(df: pd.DataFrame, label_cols) -> str:
    sep = "-" * 90
    metric_cols = [c for c in df.columns if c not in label_cols]
    header = "".join(f"{c:<18}" for c in label_cols) + "".join(f"{c:<12}" for c in metric_cols)
    lines = [header]
    for _, row in df.iterrows():
        line = "".join(f"{str(row[c]):<18}" for c in label_cols)
        line += "".join(f"{row[c]:<12.4f}" for c in metric_cols)
        lines.append(line)
    return f"{sep}\n{header}\n{sep}\n" + "\n".join(lines[1:]) + f"\n{sep}\n"


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def compare() -> None:
    comparison_dir = ensure_dir("results/comparison")
    figures_dir    = ensure_dir("figures/comparison")

    # ---- 1. Proposed vs Baseline, per dataset ----
    bvp_rows = []
    for dataset in DATASET_NAMES:
        per_dataset_rows = []
        for model_type in MODEL_TYPES:
            m = _try_load(dataset, model_type)
            if m is not None:
                per_dataset_rows.append(_row(m))
                bvp_rows.append(_row(m))
        if len(per_dataset_rows) == 2:
            df_pair = pd.DataFrame(per_dataset_rows)
            _bar_chart(
                df_pair, "Model",
                f"Proposed (Transformer-LSTM) vs Baseline (LSTM) – {dataset}",
                str(Path(figures_dir) / f"baseline_vs_proposed_{dataset}.png"),
            )

    if bvp_rows:
        df_bvp = pd.DataFrame(bvp_rows)
        df_bvp.to_csv(str(Path(comparison_dir) / "baseline_vs_proposed.csv"), index=False)
        table = "=== Proposed (Transformer-LSTM) vs Baseline (plain LSTM) ===\n"
        table += _format_table(df_bvp, ["Dataset", "Model"])
        save_text_report(table, str(Path(comparison_dir) / "baseline_vs_proposed.txt"))
        print(table)
    else:
        logger.warning("No baseline-vs-proposed results available yet.")

    # ---- 2. Proposed model across datasets ----
    dc_rows = []
    for dataset in DATASET_NAMES:
        m = _try_load(dataset, "transformer_lstm")
        if m is not None:
            dc_rows.append(_row(m))

    if dc_rows:
        df_dc = pd.DataFrame(dc_rows)
        df_dc.to_csv(str(Path(comparison_dir) / "dataset_comparison.csv"), index=False)
        table = "=== Proposed Model (Transformer-LSTM) — Dataset Comparison ===\n"
        table += _format_table(df_dc, ["Dataset", "Model"])
        save_text_report(table, str(Path(comparison_dir) / "dataset_comparison.txt"))
        print(table)

        if len(df_dc) > 1:
            _bar_chart(
                df_dc, "Dataset",
                "Proposed Model – Performance Across Datasets",
                str(Path(figures_dir) / "dataset_comparison_proposed_model.png"),
            )
    else:
        logger.warning("No proposed-model results available yet for dataset comparison.")


if __name__ == "__main__":
    compare()
